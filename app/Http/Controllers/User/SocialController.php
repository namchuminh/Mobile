    {
        $googleUser = Socialite::driver('google')->user();

        $user = $this->user->updateOrCreate([
            'email' => $googleUser->email,
        ], [
            'name' => $googleUser->name,
            'email' => $googleUser->email,
        ]);
        $user->roles()->sync(2);

        auth()->login($user);

        return redirect()->route('home.index');
    }

